package beans;

import javax.inject.Named;
import javax.enterprise.context.Dependent;

/**
 *
 * @author Disneider
 */
@Named(value = "mensaje")
@Dependent
public class beansMensaje {
    
    private String id;
    private String msg;
    /**
     * Creates a new instance of Mensaje
     */
    public beansMensaje(String id, String msg) {
        this.id = id;
        this.msg = msg;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
    
}
